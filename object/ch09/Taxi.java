package ch09;

public class Taxi {
    String taxiName = "택시";
    int taxiFare = 5000;

    public Taxi() {
    }

    public String getTaxiName() {
        return taxiName;
    }

    public void setTaxiName(String taxiName) {
        this.taxiName = taxiName;
    }

    public int getTaxiFare() {
        return taxiFare;
    }

    public void setTaxiFare(int taxiFare) {
        this.taxiFare = taxiFare;
    }
}
